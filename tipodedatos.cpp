#include <iostream>

int main() {
	int number = 10;
	std::string nombre = "Franco";
	float numberfloat = 1.10;
	char letra = 'f';
	double numberdouble = 1.111111111111111111111111;
	
	std::cout << "Mi numero entero es: " << number << std::endl;
	std::cout << "Mi nombre es: " << nombre << std::endl;
	std::cout << "Mi numero flotante es: " << numberfloat << std::endl;
	std::cout << "Mi caracter es: " << letra << std::endl;
	std::cout << "Mi numero doble es " << numberdouble << std::endl;
	return 0;
}

